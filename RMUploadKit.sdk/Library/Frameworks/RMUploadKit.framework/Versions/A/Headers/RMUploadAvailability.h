#import <Cocoa/Cocoa.h>

#define RMUPLOADKIT_DEPRECATED_ATTRIBUTE __attribute__((deprecated))
#define RMUPLOADKIT_UNAVAILABLE_ATTRIBUTE __attribute__((unavailable))
#define RMUPLOADKIT_WEAK_IMPORT_ATTRIBUTE __attribute__((weak_import))

#define RMUPLOADKIT_FRAMEWORK_VERSION_1 0100
#define RMUPLOADKIT_FRAMEWORK_VERSION_2 0200
#define RMUPLOADKIT_FRAMEWORK_VERSION_3 0300

#ifndef RMUPLOADKIT_FRAMEWORK_VERSION_MIN_REQUIRED
#define RMUPLOADKIT_FRAMEWORK_VERSION_MIN_REQUIRED RMUPLOADKIT_FRAMEWORK_VERSION_3
#endif

#ifndef RMUPLOADKIT_FRAMEWORK_VERSION_MAX_ALLOWED
#define RMUPLOADKIT_FRAMEWORK_VERSION_MAX_ALLOWED RMUPLOADKIT_FRAMEWORK_VERSION_3
#endif

#if RMUPLOADKIT_FRAMEWORK_VERSION_MAX_ALLOWED < RMUPLOADKIT_FRAMEWORK_VERSION_3
	#define AVAILABLE_RMUPLOADKIT_VERSION_3_AND_LATER     UNAVAILABLE_ATTRIBUTE
#elif RMUPLOADKIT_VERSION_MIN_REQUIRED < RMUPLOADKIT_VERSION_3
	#define AVAILABLE_RMUPLOADKIT_VERSION_3_AND_LATER     WEAK_IMPORT_ATTRIBUTE
#else
	#define AVAILABLE_RMUPLOADKIT_VERSION_3_AND_LATER
#endif
